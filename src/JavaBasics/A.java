package JavaBasics;

public class A {
public A() {
System.out.println(" Parent Class COnstructor");	
}

public A(int i) {
System.out.println(" Parent Class COnstructor with 1 parameter " +i);	
}

public A(int i, int j) {
System.out.println(" Parent Class COnstructor with 2 parameters i = " +i);	
System.out.println(" Parent Class COnstructor with 2 parameters j = " +j);	
}
}
